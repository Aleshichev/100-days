from turtle import Turtle, Screen     # черепаха, экран

timmy = Turtle()    # создали новый объект
timmy.shape("turtle")    # методы связанные с объектом
timmy.color("red")        # методы связанные с объектом
timmy.forward(100)        # методы связанные с объектом
my_screen = Screen()     # запускает окно экран
my_screen.exitonclick()    #программа работает до нажатия кнопки


print(my_screen.canvheight)    #точка имени объекта как переменная